#include <iostream>
#include <cmath>
using namespace std;

void arithmetic(){
	cout << "25. arithmetic(): " << endl;
	cout << "sqrt(15.0): " << sqrt(15.0) << endl;
	cout << "sqrt(15): " << sqrt(15) << endl;
	cout << "abs(-3): " << abs(-3) << endl;
	cout << "abs(3): " << abs(3) << endl;
	cout << "abs(0): " << abs(0) << endl;
	cout << "fabs(-3.5): " << fabs(-3.5) << endl;
	cout << "fabs(3.5): " << fabs(3.5) << endl;
	cout << "floor(5.8): " << floor(5.8) << endl;
	cout << "floor(5.1): " << floor(5.1) << endl;
	cout << "ceil(5.8): " << ceil(5.8) << endl;
	cout << "ceil(5.1): " << ceil(5.1) << endl;
	cout << "pow(2.0, 3.0): " << pow(2.0, 3.0) << endl;
	cout << "pow(1.1, 2): " << pow(1.1, 2) << endl;
}
